# –í–∫–ª–∞–¥–∫–∞ —Å –¥–∞–Ω–Ω—ã–º–∏
with t2:
    st.subheader('‚ÜïÔ∏è–£–¥–∞–ª–µ–Ω–Ω—ã —Å—Ç–æ–ª–±—Ü—ã A –∏ B')
    # –°–æ–∑–¥–∞—Ç—å –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä
