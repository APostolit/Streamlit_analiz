    # –°–æ–∑–¥–∞—Ç—å –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä
    with st.container(width=500):
        # –£–¥–∞–ª—è–µ–º —Å—Ç—Ä–æ–∫–∏ 0 –∏ 1
        df_drop_row = df.drop([0, 1], axis=0)
