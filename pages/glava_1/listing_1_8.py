st.subheader('üêº–ú–∞–Ω–∏–ø—É–ª—è—Ü–∏—è –¥–∞–Ω–Ω—ã–º–∏ DataFrame –≤ Pandas')
# –°–æ–∑–¥–∞–µ–º –≤–∫–ª–∞–¥–∫–∏ –¥–ª—è –¥–∞–Ω–Ω—ã—Ö
t1, t2, t3 = st.tabs(["üì∂–ù–∞–±–æ—Ä –¥–∞–Ω–Ω—ã—Ö DataFrame",
                      "‚ÜïÔ∏è–£–¥–∞–ª–µ–Ω–∏–µ –∫–æ–ª–æ–Ω–æ–∫",
                      "‚ÜîÔ∏è–£–¥–∞–ª–µ–Ω–∏–µ —Å—Ç—Ä–æ–∫"
                      ])

# –°–æ–∑–¥–∞–µ–º DataFrame
df = pd.DataFrame({'–ê': [10, 20, 30],
                   '–ë': [45, 55, 65],
                   '–í': [74, 85, 96],
                   '–ì': [94, 35, 66]})
# –í–∫–ª–∞–¥–∫–∞ —Å –¥–∞–Ω–Ω—ã–º–∏
