import streamlit as st
import pandas as pd

st.subheader('üêº–ì—Ä—É–ø–ø–∏—Ä–æ–≤–∫–∞ –∏ –∞–≥—Ä–µ–≥–∞—Ü–∏—è DataFrame –≤ Pandas')
# –°–æ–∑–¥–∞–µ–º –≤–∫–ª–∞–¥–∫–∏ –¥–ª—è –¥–∞–Ω–Ω—ã—Ö
t1, t2, = st.tabs(
    ["üì∂–ò—Å—Ö–æ–¥–Ω—ã–π DataFrame",
     "üç∞–°—Ä–µ–¥–Ω—è—è —Ü–µ–Ω–∞",
     ])

# –°–æ–∑–¥–∞—Ç—å –Ω–∞–±–æ—Ä –¥–∞–Ω–Ω—ã—Ö
df = pd.DataFrame({'–ü—Ä–æ–¥—É–∫—Ç': ['–ü–∏—Ä–æ–∂–Ω—ã–µ', '–ü–∏—Ä–æ–∂–Ω—ã–µ', '–ö–æ–Ω—Ñ–µ—Ç—ã', '–ö–æ–Ω—Ñ–µ—Ç—ã', '–ö–æ–Ω—Ñ–µ—Ç—ã'],
                    '–ù–∞–∑–≤–∞–Ω–∏–µ': ['–®–æ–∫–æ–ª–∞–¥–Ω–æ–µ', '–í–∞–Ω–∏–ª—å–Ω–æ–µ', '–ë–µ–ª–æ—á–∫–∞', '–ì—Ä–∏–ª—å—è–∂', '–õ–∞—Å—Ç–æ—á–∫–∞'],
                    '–¶–µ–Ω–∞': [80, 90, 300, 400, 200]})

# –í–∫–ª–∞–¥–∫–∞ —Å –¥–∞–Ω–Ω—ã–º–∏
with t1:
    st.subheader('üì∂–ò—Å—Ö–æ–¥–Ω—ã–π DataFrame')
    # –°–æ–∑–¥–∞—Ç—å –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä
    with st.container(width=500):
        st.dataframe(df)

# –í–∫–ª–∞–¥–∫–∞ —Å –¥–∞–Ω–Ω—ã–º–∏
with t2:
    st.subheader('üç∞–°—Ä–µ–¥–Ω—è—è —Ü–µ–Ω–∞ –ø—Ä–æ–¥—É–∫—Ç–∞')
    # –°–æ–∑–¥–∞—Ç—å –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä
    with st.container(width=300):
        # –ì—Ä—É–ø–ø–∏—Ä—É–µ–º –¥–∞–Ω–Ω—ã–µ –ø–æ —Å—Ç–æ–ª–±—Ü—É '–ü—Ä–æ–¥—É–∫—Ç' –∏ –≤—ã—á–∏—Å–ª—è–µ–º —Å—Ä–µ–¥–Ω—é—é —Ü–µ–Ω—É
        grouped_data = df.groupby('–ü—Ä–æ–¥—É–∫—Ç').agg({'–¶–µ–Ω–∞': 'mean'})
        st.dataframe(grouped_data)